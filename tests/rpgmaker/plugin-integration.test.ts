function makeTempProject(): string {
  const dir = fs.mkdtempSync(path.join(os.tmpdir(), "rpgmaker-plugin-test-"));
  const dataDir = path.join(dir, "data");
  const pluginsDir = path.join(dir, "js", "plugins");
  fs.mkdirSync(dataDir, { recursive: true });
  fs.mkdirSync(pluginsDir, { recursive: true });

  // Minimal required data files
  fs.writeFileSync(path.join(dataDir, "Troops.json"),  JSON.stringify([null]), "utf-8");
  fs.writeFileSync(path.join(dataDir, "Enemies.json"), JSON.stringify([null]), "utf-8");
  fs.writeFileSync(path.join(dataDir, "System.json"),  JSON.stringify({ gameTitle: "Test", versionId: 1 }), "utf-8");

  // Minimal plugins.js (empty list — simulates a fresh project)
  const jsDir = path.join(dir, "js");
  fs.writeFileSync(
    path.join(jsDir, "plugins.js"),
    "// Generated by RPG Maker.\n// Do not edit this file directly.\nvar $plugins =\n[];\n",
    "utf-8",
  );

  return dir;
}

function makeContext(dir: string, bridge?: RPGMakerDebugBridge): HandlerContext {
  return {
    projectPath: dir,
    reader: new RPGMakerReader({ projectPath: dir }),
    writer: new RPGMakerWriter({ projectPath: dir, createBackup: false }),
    debugBridge: bridge ?? new RPGMakerDebugBridge(),
    changeLog: new ChangeLog(dir),
    input: {},
    debug: false,
  };
}

