describe("handleRunBattleSuite", () => {
  let dir: string;
  beforeEach(() => { dir = makeTempProject(); });
  afterEach(() => fs.rmSync(dir, { recursive: true, force: true }));

  function makeFinalState(actorHp = 300) {
    return {
      inBattle: false, battleOver: true, turn: 3,
      actors: [{ name: "Reid", hp: actorHp, mhp: 544, mp: 80, mmp: 80, states: [] }],
      enemies: [{ name: "Duende", hp: 0, mhp: 200, mp: 0, mmp: 0, states: [] }],
    };
  }

  function autoResolvingBridge(runs: number, winResult = 0) {
    const bridge = new RPGMakerDebugBridge();
    bridge.markConnected();
    let callCount = 0;
    const origSet = bridge.setCommand.bind(bridge);
    bridge.setCommand = (cmd, args) => {
      origSet(cmd, args);
      if (cmd === "set_party_state") {
        setTimeout(() => bridge.resolveAck(), 20);
      } else if (cmd === "start_battle") {
        setTimeout(() => {
          bridge.addEvent({ type: "battle_start", troopId: args.troopId });
          bridge.addEvent({ type: "action", subject: "Reid", subject_type: "actor", action: "Attack",
            target: "Duende", hpDamage: 150, hpBefore: 200, hpAfter: 50, alive: true, critical: false, formula: "a.atk", turn: 1 });
          bridge.addEvent({ type: "action", subject: "Duende", subject_type: "enemy", action: "Attack",
            target: "Reid", hpDamage: 80, hpBefore: 544, hpAfter: 464, alive: true, critical: false, formula: "a.atk", turn: 1 });
          bridge.addEvent({ type: "battle_end", result: callCount++ < runs ? winResult : 2 });
          bridge.setFinalState(makeFinalState(winResult === 0 ? 300 : 0));
        }, 30);
      }
    };
    return bridge;
  }

  it("returns error when not connected", async () => {
    const ctx = makeContext(dir, new RPGMakerDebugBridge());
    const result = JSON.parse(await handleRunBattleSuite({ ...ctx, input: { troop_id: 1, runs: 2 } }));
    expect(result.error).toMatch(/not connected/i);
  });

  it("returns error without troop_id or enemy_id", async () => {
    const bridge = new RPGMakerDebugBridge();
    bridge.markConnected();
    const ctx = makeContext(dir, bridge);
    const result = JSON.parse(await handleRunBattleSuite({ ...ctx, input: { runs: 3 } }));
    expect(result.error).toMatch(/troop_id|enemy_id/i);
  });

  it("runs the requested number of battles", async () => {
    const bridge = autoResolvingBridge(3);
    const ctx = makeContext(dir, bridge);
    const result = JSON.parse(await handleRunBattleSuite({ ...ctx, input: { troop_id: 1, runs: 3 } }));
    expect(result.success).toBe(true);
    expect(result.runs).toBe(3);
    expect(result.summary.total_runs).toBe(3);
  });

  it("caps runs at 50", async () => {
    vi.useFakeTimers();
    try {
      const bridge = new RPGMakerDebugBridge();
      bridge.markConnected();
      bridge.setCommand = () => {};
      bridge.waitForAck = async () => true;
      bridge.waitForBattle = async () => ({
        success: true,
        log: [{ type: "battle_end", result: 0 }],
        state: makeFinalState(),
        summary: "ok",
      });
      const ctx = makeContext(dir, bridge);
      const handlePromise = handleRunBattleSuite({ ...ctx, input: { troop_id: 1, runs: 999 } });
      await vi.runAllTimersAsync();
      const result = JSON.parse(await handlePromise);
      expect(result.success).toBe(true);
      expect(result.runs).toBe(50);
    } finally {
      vi.useRealTimers();
    }
  });

  it("computes win_rate correctly", async () => {
    const bridge = new RPGMakerDebugBridge();
    bridge.markConnected();
    const origSet = RPGMakerDebugBridge.prototype.setCommand.bind(bridge);
    let callIdx = 0;
    bridge.setCommand = (cmd: string, args: Record<string, unknown>) => {
      origSet(cmd, args); // resets events/finalState each run
      if (cmd === "set_party_state") {
        setTimeout(() => bridge.resolveAck(), 20);
      } else if (cmd === "start_battle") {
        const win = callIdx++ < 3; // 3 wins, 1 loss
        setTimeout(() => {
          bridge.addEvent({ type: "battle_end", result: win ? 0 : 2 });
          bridge.setFinalState(makeFinalState(win ? 300 : 0));
        }, 30);
      }
    };
    const ctx = makeContext(dir, bridge);
    const result = JSON.parse(await handleRunBattleSuite({ ...ctx, input: { troop_id: 1, runs: 4 } }));
    expect(result.summary.wins).toBe(3);
    expect(result.summary.losses).toBe(1);
    expect(result.summary.win_rate).toBe(0.75);
  });

  it("aggregates actor HP stats", async () => {
    const bridge = autoResolvingBridge(2);
    const ctx = makeContext(dir, bridge);
    const result = JSON.parse(await handleRunBattleSuite({ ...ctx, input: { troop_id: 1, runs: 2 } }));
    const reid = result.summary.actors.find((a: { name: string }) => a.name === "Reid");
    expect(reid).toBeDefined();
    expect(typeof reid.avg_hp).toBe("number");
    expect(typeof reid.avg_hp_percent).toBe("number");
    expect(typeof reid.survival_rate).toBe("number");
  });

  it("splits damage into dealt/taken using subject_type", async () => {
    const bridge = autoResolvingBridge(1);
    const ctx = makeContext(dir, bridge);
    const result = JSON.parse(await handleRunBattleSuite({ ...ctx, input: { troop_id: 1, runs: 1 } }));
    expect(result.summary.damage.avg_dealt).toBe(150); // Reid dealt 150
    expect(result.summary.damage.avg_taken).toBe(80);  // Duende dealt 80
  });

  it("passes party_state to set_party_state each run", async () => {
    const bridge = autoResolvingBridge(2);
    const capturedStates: Record<string, unknown>[] = [];
    const origSet = bridge.setCommand.bind(bridge);
    bridge.setCommand = (cmd, args) => {
      if (cmd === "set_party_state") capturedStates.push({ ...args });
      origSet(cmd, args);
    };
    const ctx = makeContext(dir, bridge);
    await handleRunBattleSuite({ ...ctx, input: { troop_id: 1, runs: 2, party_state: { hp_percent: 0.5 } } });
    expect(capturedStates).toHaveLength(2);
    expect(capturedStates.every(s => s.hp_percent === 0.5)).toBe(true);
  });

  it("defaults party_state to full HP/MP when omitted", async () => {
    const bridge = autoResolvingBridge(1);
    let capturedState: Record<string, unknown> = {};
    const origSet = bridge.setCommand.bind(bridge);
    bridge.setCommand = (cmd, args) => { if (cmd === "set_party_state") capturedState = { ...args }; origSet(cmd, args); };
    const ctx = makeContext(dir, bridge);
    await handleRunBattleSuite({ ...ctx, input: { troop_id: 1, runs: 1 } });
    expect(capturedState.hp_percent).toBe(1.0);
    expect(capturedState.mp_percent).toBe(1.0);
  });

  it("appends a change log entry with win/loss summary", async () => {
    const bridge = autoResolvingBridge(2);
    const ctx = makeContext(dir, bridge);
    await handleRunBattleSuite({ ...ctx, input: { troop_id: 1, runs: 2 } });
    const entries = ctx.changeLog.read({ limit: 5 });
    const entry = entries.find(e => e.tool === "run-battle-suite");
    expect(entry).toBeDefined();
    expect(entry!.summary).toContain("W/");
  });

  it("plugin template uses canLose=true in BattleManager.setup", () => {
    const code = PluginTemplates.debugBridge(9001);
    expect(code).toContain("BattleManager.setup(troopId, true, true)");
  });

  it("plugin template includes subject_type in action events", () => {
    const code = PluginTemplates.debugBridge(9001);
    expect(code).toContain("subject_type");
    expect(code).toContain("isActorSide");
  });
});
