describe("HTTP bridge endpoint simulation", () => {
  it("/ping with no command returns 204", async () => {
    const bridge = new RPGMakerDebugBridge();
    // Simulate what the HTTP handler does for GET /ping
    bridge.markConnected();
    const cmd = bridge.getCommand();
    expect(cmd).toBeNull();
    // 204 = no content
  });

  it("/ping with pending command returns the command JSON", async () => {
    const bridge = new RPGMakerDebugBridge();
    bridge.setCommand("set_switch", { id: 3, value: true });
    const cmd = bridge.getCommand();
    expect(cmd).not.toBeNull();
    expect(JSON.stringify(cmd)).toContain("set_switch");
  });

  it("/ack resolves the pending waiter AND clears the command", async () => {
    const bridge = new RPGMakerDebugBridge();
    bridge.setCommand("set_variable", { id: 1, value: 100 });

    let ackResolved = false;
    const waitPromise = bridge.waitForAck(2000).then((v) => { ackResolved = v; });

    // Simulate game posting to /ack
    bridge.resolveAck();
    await waitPromise;

    expect(ackResolved).toBe(true);
    expect(bridge.getCommand()).toBeNull(); // cleared by resolveAck
  });

  it("/gamestate resolves waitForGameState and clears command", async () => {
    const bridge = new RPGMakerDebugBridge();
    bridge.setCommand("get_state", {});

    const state: GameState = {
      mapId: 5, playerX: 1, playerY: 2, gold: 0,
      partyMembers: [], inBattle: false,
      timestamp: new Date().toISOString(),
    };

    let resolved: GameState | null = null;
    const waitPromise = bridge.waitForGameState(2000).then((s) => { resolved = s; });

    // Simulate game posting to /gamestate
    bridge.setGameState(state);
    await waitPromise;

    expect(resolved!.mapId).toBe(5);
    expect(bridge.getCommand()).toBeNull(); // cleared by waitForGameState
  });

  it("/log appends events visible to waitForBattle", async () => {
    const bridge = new RPGMakerDebugBridge();
    bridge.setCommand("start_battle", { troopId: 1 });

    const battlePromise = bridge.waitForBattle(2000);

    bridge.addEvent({ type: "battle_start", troopId: 1 });
    bridge.addEvent({ type: "action", subject: "Hero", target: "Slime" });
    bridge.setFinalState({ inBattle: false, battleOver: true, turn: 2, actors: [], enemies: [] });

    const result = await battlePromise;
    expect(result.success).toBe(true);
    expect(result.log.length).toBeGreaterThanOrEqual(2);
    expect(result.log[0].type).toBe("battle_start");
  });

  it("/state with inBattle=true does NOT resolve waitForBattle", async () => {
    const bridge = new RPGMakerDebugBridge();
    bridge.setCommand("start_battle", { troopId: 1 });

    // Post a mid-battle state (still fighting)
    bridge.setFinalState({ inBattle: true, battleOver: false, turn: 1, actors: [], enemies: [] });

    // waitForBattle should NOT resolve yet (state has inBattle=true)
    const earlyResolved = await Promise.race([
      bridge.waitForBattle(50).then(() => "resolved"),
      new Promise<string>((r) => setTimeout(() => r("pending"), 80)),
    ]);
    expect(earlyResolved).toBe("pending");
  });

  it("multiple sequential ack waiters resolve one at a time", async () => {
    const bridge = new RPGMakerDebugBridge();
    const order: number[] = [];

    const p1 = bridge.waitForAck(2000).then(() => order.push(1));
    const p2 = bridge.waitForAck(2000).then(() => order.push(2));

    bridge.resolveAck(); // resolves p1
    await p1;
    expect(order).toEqual([1]);

    bridge.resolveAck(); // resolves p2
    await p2;
    expect(order).toEqual([1, 2]);
  });
});

